// + : 이항연산자
// = : 대입연산자
// > < == === : 비교연산자(이것은 결과값으로 true, false을 호출한다.

console.log(1+2); // 이항연산자 사용예 결과값 3
var a = 1; // 대입연산자 사용예 a의 변수에 1의 상수를 대입한다.
console.log(a); // 대입연산자 사용예 결과값 1
console.log( 1 == 2) // 비교연산자 사용예 결과값 false
console.log( 1 === 1) // 비교연산자 사용예 결과값 true
console.log( 1 < 2) // 비교연산자 사용예 결과값 true